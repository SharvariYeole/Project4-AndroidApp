package com.sharvari.project4;

public class DisplayItem {
    String count;
    String title;
    String author;
    String date;

    DisplayItem(String count,
                String title,
             String author,
             String date)
    {
        this.count = count;
        this.title = title;
        this.author = author;
        this.date = date;
    }
}
